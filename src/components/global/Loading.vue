<template>
	<div class="loading" v-if="show">
		<div class="spinner">
  <div class="bounce1"></div>
  <div class="bounce2"></div>
  <div class="bounce3"></div>
</div>
	</div>	
</template>
<script>
	export default{
		name: 'loading',
		data(){
			return{
				show: false
			}
		},
		mounted(){
			this.$bus.$on('setLoadingScreen', this.setLoadingScreen);
			this.$bus.$on('hideLoadingScreen', this.hideLoadingScreen);

		},
		methods: {
			setLoadingScreen: function(){
				this.show = true;
			},
			hideLoadingScreen: function(){
				this.show = false;
			}
		}
	}
</script>
